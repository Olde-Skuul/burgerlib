def setup(app):
    """
    Called by breathe to create the doxygen docs
    """

    # Add hook for building doxygen xml when needed
    app.connect("builder-inited", generate_doxygen_xml)

    # Add hook for building doxygen xml when needed
    app.connect("build-finished", copy_docs)
