// If negative, use as is. Otherwise get the warning message
// Note, this MUST be resolvable at compile time since it
// generates compile time constants
#define CONVERT_HRESULT(x) \
	((HRESULT)(x) <= 0 ? \
			((HRESULT)(x)) : \
			((HRESULT)(((x)&0xFFFF) | (FACILITY_WIN32 << 16) | 0x80000000)))

// Convert a literal number into a string
#define ERR_LITERAL(x) \
	{ \
		static_cast<HRESULT>(x), #x \
	}

// Convert both a positive Win32 error and it's negative counterpart
#define ERR_WIN32(x) \
	{CONVERT_HRESULT(x), #x}, \
	{ \
		x, #x \
	}

// Literal substitution
#define ERR_LITERAL_STRING(x, s) \
	{ \
		CONVERT_HRESULT(x), #s \
	}

